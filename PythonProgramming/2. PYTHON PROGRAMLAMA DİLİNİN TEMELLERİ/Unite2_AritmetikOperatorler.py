# Toplama # + # x+y=10
# Çıkarma # - # x-y=2
# Çarpma # * # x*y=24
# Bölme # / # x/y=1.5
# Üs # ** # x**y=1296
# Bölümden kalan (mod) # % # x%y=2
# Tam Sayı Bölme # // # x//y=1

x=6
y=4
print('x+y= ',x+y) #Toplama islemi
print('x-y= ',x-y) #Cikarma islemi
print('x*y= ',x*y) #Carpma islemi
print('x/y= ',x/y) #Bolme islemi
print('x//y= ',x//y) #Tamsayi bolme islemi
print('x**y= ',x**y) #Us alma islemi
print('x%y= ',x%y) #Bolumden kalan islemi

