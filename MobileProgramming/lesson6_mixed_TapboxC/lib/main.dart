import 'package:flutter/material.dart';

void main() {
  runApp(const MyApp());
}

//---------------------------- ParentWidget ----------------------------
//Melez Yaklaşımla Durum Yönetimi

class ParentWidget extends StatefulWidget {
  const ParentWidget({super.key});

  @override
  State<ParentWidget> createState() => _ParentWidgetState();
}

class _ParentWidgetState extends State<ParentWidget> {
  bool _active = false;

  void _handleTapboxChanged(bool newValue) {
    setState(() {
      _active = newValue;
    });
  }

  @override
  Widget build(BuildContext context) {
    return SizedBox(
      child: TapboxC(
        active: _active,
        onChanged: _handleTapboxChanged,
      ),
    );
  }
}

//----------------------------- TapboxC ------------------------------

class TapboxC extends StatefulWidget {
  const TapboxC({
    super.key,
    this.active = false,
    required this.onChanged,
  });

  final bool active;
  final ValueChanged<bool> onChanged;

  @override
  State<TapboxC> createState() => _TapboxCState();
}

class _TapboxCState extends State<TapboxC> { //Statefull tanimlandigi icin state yaziliyor
  bool _highlight = false;

  void _handleTapDown(TapDownDetails details) {//Tiklamanin baslamasi
    setState(() {
      _highlight = true;
    });
  }

  void _handleTapUp(TapUpDetails details) { //Tiklamanin bitirilmesi
    setState(() {
      _highlight = false;
    });
  }

  void _handleTapCancel() { //Tiklamanin kaydilarak bitirilmesi
    setState(() {
      _highlight = false;
    });
  }

  void _handleTap() {//Tiklama islemi yapilmasi/tiklama sureci
    widget.onChanged(!widget.active); //statefull olarak tanimlanan widget'ın onChanged paramatresi yakalanarak durumun degistirilmesi saglaniyor
  }

  @override
  Widget build(BuildContext context) {
    // This example adds a green border on tap down.
    // On tap up, the square changes to the opposite state.
    return GestureDetector(
      onTapDown: _handleTapDown, // Handle the tap events in the order that
      onTapUp: _handleTapUp, // they occur: down, up, tap, cancel
      onTap: _handleTap,
      onTapCancel: _handleTapCancel,
      child: Container(
        width: 200,
        height: 200,
        decoration: BoxDecoration(
          color: widget.active ? Colors.lightGreen[700] : Colors.grey[600],
          border: _highlight
              ? Border.all(
                  color: Colors.teal[700]!,
                  width: 10,
                )
              : null,
        ),
        child: Center(
          child: Text(widget.active ? 'Active' : 'Inactive',
              style: const TextStyle(fontSize: 32, color: Colors.white)),
        ),
      ),
    );
  }
}

//------------------------- MyApp ----------------------------------

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Flutter Demo',
      home: Scaffold(
        appBar: AppBar(
          title: const Text('Flutter Demo'),
        ),
        body: const Center(
          child: ParentWidget(),
        ),
      ),
    );
  }
}